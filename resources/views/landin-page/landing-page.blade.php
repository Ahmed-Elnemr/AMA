                availableHeight -= self.verticalPadding;

                if (enableScaleUp) {
                    newScale = availableHeight / elementHeight;
                } else if (elementHeight > availableHeight) {
                    newScale = availableHeight / elementHeight;
                }

                return newScale;
            }

